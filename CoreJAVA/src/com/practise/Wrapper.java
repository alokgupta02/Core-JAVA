package com.practise;

public class Wrapper {

	public static void main(String[] args) {

		
		Integer i = 20;
		System.out.println(i);
		
		int a  = 10;
		//autoboxing
		Integer n = a;
		System.out.println(n);
		
		Integer m = 25;
		//unboxing
		int p = m;
		System.out.println(p);
	}

}
